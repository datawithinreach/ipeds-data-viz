import { access, readdir, writeFile } from 'node:fs/promises';
import { constants as fsConstants } from 'node:fs';
import path from 'node:path';

const rootDir = process.cwd();
const articleDir = path.join(rootDir, 'app', 'article');
const registryPath = path.join(articleDir, 'registry.ts');

async function folderHasRegistryContent(slug) {
  for (const file of ['article.mdx', 'content.ts']) {
    try {
      await access(path.join(articleDir, slug, file), fsConstants.F_OK);
      return true;
    } catch {
      /* try next */
    }
  }
  return false;
}

/** Prefer MDX when present so registry matches the authoring surface. */
async function getRegistryModulePath(slug) {
  const mdxPath = path.join(articleDir, slug, 'article.mdx');
  try {
    await access(mdxPath, fsConstants.F_OK);
    return `./${slug}/article.mdx`;
  } catch {
    return `./${slug}/content`;
  }
}

async function getArticleSlugs() {
  const entries = await readdir(articleDir, { withFileTypes: true });

  const directories = entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name);

  const slugs = [];
  for (const slug of directories) {
    if (await folderHasRegistryContent(slug)) {
      slugs.push(slug);
    }
  }

  return slugs.sort((a, b) => a.localeCompare(b));
}

async function buildRegistrySource(slugs) {
  const imports = [];
  const metaLines = [];
  for (let index = 0; index < slugs.length; index++) {
    const slug = slugs[index];
    const mod = await getRegistryModulePath(slug);
    imports.push(`import * as _Article${index} from '${mod}';`);
    metaLines.push(
      `const ArticleMeta${index} = (_Article${index} as unknown as { ArticleMeta: ArticleMetaType }).ArticleMeta;`,
    );
  }

  const entries = slugs
    .map(
      (slug, index) =>
        `  { slug: '${slug}', ArticleMeta: ArticleMeta${index} },`,
    )
    .join('\n');

  return `/* eslint-disable */
// This file is auto-generated by scripts/generate-article-registry.mjs.
// Do not edit manually.

import type { ArticleMeta as ArticleMetaType } from '@/components/article/types';
${imports.join('\n')}
${metaLines.join('\n')}

export const articleRegistry = [
${entries}
];
`;
}

async function main() {
  const slugs = await getArticleSlugs();
  const source = await buildRegistrySource(slugs);
  await writeFile(registryPath, source, 'utf8');
  console.log(`Generated article registry with ${slugs.length} entries.`);
}

main().catch((error) => {
  console.error('Failed to generate article registry.');
  console.error(error);
  process.exit(1);
});

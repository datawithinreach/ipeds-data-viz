import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

// The script runs from the project root, so process.cwd() gives us
// an absolute base path we can safely join from.
const rootDir = process.cwd();
// Directory that contains one folder per article slug:
// Example: app/article/admissions-top-20, app/article/financial-aid-top-20
const articleDir = path.join(rootDir, 'app', 'article');
// Output file this script generates.
const registryPath = path.join(articleDir, 'registry.ts');

/**
 * Read all article folders and return sorted slug names.
 *
 * Example:
 * - If app/article contains:
 *   - admissions-top-20/
 *   - financial-aid-top-20/
 *   - registry.ts (file, not a folder)
 * - This returns: ['admissions-top-20', 'financial-aid-top-20']
 */
async function getArticleSlugs() {
  // Read directory entries with file type metadata so we can check
  // "is this a folder?" without extra filesystem calls.
  const entries = await readdir(articleDir, { withFileTypes: true });

  return entries
    // Keep only article folders (skip files like registry.ts).
    .filter((entry) => entry.isDirectory())
    // Folder name becomes the article slug.
    .map((entry) => entry.name)
    // Sort for deterministic output (stable diffs in git).
    .sort((a, b) => a.localeCompare(b));
}

/**
 * Build the TypeScript source for app/article/registry.ts.
 *
 * Example (input):
 *   slugs = ['admissions-top-20', 'financial-aid-top-20']
 *
 * Example (generated imports section):
 *   import { ArticleMeta as ArticleMeta0 } from './admissions-top-20/content';
 *   import { ArticleMeta as ArticleMeta1 } from './financial-aid-top-20/content';
 *
 * Example (generated registry entry section):
 *   { slug: 'admissions-top-20', ArticleMeta: ArticleMeta0 },
 *   { slug: 'financial-aid-top-20', ArticleMeta: ArticleMeta1 },
 */
function buildRegistrySource(slugs) {
  // Each article exports `ArticleMeta`, so we alias them to unique names
  // (ArticleMeta0, ArticleMeta1, ...) to avoid import name collisions.
  const imports = slugs
    .map(
      (slug, index) =>
        `import { ArticleMeta as ArticleMeta${index} } from './${slug}/content';`,
    )
    .join('\n');

  // Build one object per article for the registry array.
  const entries = slugs
    .map(
      (slug, index) =>
        `  { slug: '${slug}', ArticleMeta: ArticleMeta${index} },`,
    )
    .join('\n');

  // Return the final file contents as one string.
  // Note: this intentionally includes a generated-file warning header.
  return `/* eslint-disable */
// This file is auto-generated by scripts/generate-article-registry.mjs.
// Do not edit manually.

${imports}

export const articleRegistry = [
${entries}
];
`;
}

async function main() {
  // 1) Discover article slugs from folders.
  const slugs = await getArticleSlugs();
  // 2) Convert those slugs into TypeScript source code.
  const source = buildRegistrySource(slugs);
  // 3) Write the generated source to app/article/registry.ts.
  await writeFile(registryPath, source, 'utf8');
  // 4) Print a quick success message for visibility in terminal/CI logs.
  console.log(`Generated article registry with ${slugs.length} entries.`);
}

// Top-level execution with a catch so failures produce a useful message
// and a non-zero exit code for scripts/CI.
main().catch((error) => {
  console.error('Failed to generate article registry.');
  console.error(error);
  process.exit(1);
});
